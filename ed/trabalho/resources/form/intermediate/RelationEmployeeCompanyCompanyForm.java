  private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
      //Can use only one of input per person (ID OR Email)
      if (inputRole.getText().isEmpty()) {
          this.message("Need to insert the role to search.");
          return;
      } else if (inputCompanyFrom.getText().isEmpty() || inputCompanyTo.getText().isEmpty()) {
          this.message("Need to insert companies to search.");
          return;
      }

      //Create list of people in companys with role
      String role = inputRole.getText();
      String companyFrom = inputCompanyFrom.getText();
      String companyTo = inputCompanyTo.getText();

      DynamicArrayContract resultList = this.getRelationsOfEmployeesWithRoleInCompanies(role, companyFrom, companyTo);

      //Show result in new frame
      PeopleListView view = new PeopleListView();
      view.setTitle("Relation of Employees of Companies Roles");
      view.setDesc("(Ex)Employees from company '" + companyFrom + "' with role '" + role + "' and their relation with employees of company '" + companyTo + "' with the same role.");
      view.loadPeople(resultList);
      view.pack();
      view.setVisible(true);
  }//GEN-LAST:event_submitButtonActionPerformed


